#include <stdio.h>

/*
	Write a C program to print a block F using hash (#), where the F has a height of six characters and width of five and four characters.
	Expected Output:
	######
	#
	#
	#####
	#
	#
	#
*/

int main(int argc, char **argv) {
	printf("######\n");
	printf("#\n");
	printf("#\n");
	printf("####\n");
	printf("#\n");
	printf("#\n");
	printf("#");
	return 0;
}
